      double precision function dot(a,b,n)
      implicit none
      integer k,n
      real*8 a(*),b(*)
c....dot product function
      dot = 0.0d0
      do 10 k = 1,n
        dot = dot + a(k)*b(k)
 10   continue
      return
      end
