      SUBROUTINE EOMDRV(CCTYP,BESTEOM)
      DOUBLE PRECISION CCTYP,BESTEOM,EOMCC
#if !defined(SPEC_CPU_NO_HOLLERITH)
      DATA EOMCC/8HEOM-CCSD/
#else
      CHARACTER*8 :: EOMCC_STR
      EQUIVALENCE (EOMCC, EOMCC_STR)
      DATA EOMCC_STR/"EOM-CCSD"/
#endif
