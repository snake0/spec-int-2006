     *    10X,'FREQUENCY',3X,'ENTROPY',3X,'%-CONTRIBUTION'/
     *    10X,9(1H-),3X,7(1H-),3X,14(1H-))
 9060 FORMAT(11X,F7.2,3X,F7.3,10X,F6.2)
 9061 FORMAT(11X,33("-"))
