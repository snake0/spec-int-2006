C*MODULE VIBANL  *DECK EFGRDM
      SUBROUTINE EFGRDM(NCOORD,NCF,NFRG,G,RTRMS,GM,IOPT)
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C
      DIMENSION G(*),RTRMS(*),GM(*)
C
      PARAMETER (ZERO=0.0D+00)
C
      IF (NFRG.EQ.0) RETURN
C
C     IOPT=0: TO MASS-WEIGHTED
C     IOPT=1: TO CARTESSIAN
C
      NCF2=NCF/2
      K0=NCOORD
      K1=NCOORD
      DO 900 I=1,NFRG
      DO 500 J=1,3
      K0=K0+1
      K1=K1+1
      IF (IOPT.EQ.0) THEN
      GM(K1)=G(K1)*RTRMS(K0)
      ELSE
      GM(K1)=G(K1)/RTRMS(K0)
      END IF
  500 CONTINUE
      L=K1
      DO 800 J=1,3
      K1=K1+1
      JDX=NCOORD+NCF2+(I-1)*9+IOPT*9*NFRG+(J-1)*3
      DUM=ZERO
      DO 600 M=1,3
      DUM=DUM+RTRMS(JDX+M)*G(L+M)
  600 CONTINUE
      GM(K1)=DUM
  800 CONTINUE
  900 CONTINUE
C
      RETURN
      END
