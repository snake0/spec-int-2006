      SUBROUTINE DECVIB(VEC,E,FINT,EM,D,B,E1,P,IZMAT,NC1,TFACT,
     *                  SCLFAC,NFIRST,NLAST)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION VEC(NC1,NC1),E(NC1),FINT(NROW,NROW),EM(NVAR,NC1),
