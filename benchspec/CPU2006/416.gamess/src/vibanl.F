C*MODULE VIBANL  *DECK CENMAS
      SUBROUTINE CENMAS(NFRG,COM,ZMASST,CMASS)
C
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
C
      DIMENSION CMASS(3),COM(3,*)
C
      PARAMETER (MXATM=500)
      PARAMETER (MXPT=100, MXFRG=50, MXFGPT=MXPT*MXFRG)
C
      COMMON /FGRAD / DEF(3,MXFGPT),DEFT(3,MXFRG),TORQ(3,MXFRG),
     *                EFCENT(3,MXFRG),FRGMAS(MXFRG),FRGMI(6,MXFRG),
     *                ATORQ(3,MXFRG)
      COMMON /INFOA / NAT,ICH,MUL,NUM,NQMT,NE,NA,NB,
     *                ZAN(MXATM),C(3,MXATM)
      COMMON /MASSES/ ZMASS(MXATM)
C
      PARAMETER (ZERO=0.0D+00, ONE=1.0D+00)
C
C     CALCULATE TOTAL MASS AND CENTER OF MASS
C
      ZMASST=ZERO
      CALL VCLR(CMASS,1,3)
C
      DO 200 I=1,NAT
        AMASS=ZMASS(I)
        ZMASST=ZMASST+AMASS
        DO 100 J=1,3
          CMASS(J)=CMASS(J)+AMASS*C(J,I)
  100   CONTINUE
  200 CONTINUE
C
      DO 400 I=1,NFRG
        AMASS=FRGMAS(I)
        ZMASST=ZMASST+AMASS
        DO 300 J=1,3
          CMASS(J)=CMASS(J)+AMASS*EFCENT(J,I)
  300   CONTINUE
  400 CONTINUE
C
      DO 500 I=1,3
        CMASS(I)=CMASS(I)/ZMASST
  500 CONTINUE
C
C     GET CENTER OF MASS COORDINATES IN MASS-WEIGHTED CARTESIAN
C
      DO 700 I=1,NAT
        IF (ZMASS(I).GT.ZERO) THEN
          AMASS = SQRT(ZMASS(I))
        ELSE
          AMASS = ONE
        END IF
        DO 600 J=1,3
          COM(J,I) = AMASS * (C(J,I)-CMASS(J))
  600   CONTINUE
  700 CONTINUE
C
      II=NAT
      DO 900 I=1,NFRG
        II=II+1
        AMASS = SQRT(FRGMAS(I))
        DO 800 J=1,3
          COM(J,II) = AMASS * (EFCENT(J,I)-CMASS(J))
  800   CONTINUE
  900 CONTINUE
C
      RETURN
      END
