  860       CONTINUE
  870    CONTINUE
  880 CONTINUE
      WRITE(IP,8050)
C
C     ----- THERMOCHEMISTRY ANALYSIS -----
C
  900 CONTINUE
      CALL THERMO(IW,NIMAG,ZMASST,CC,COM,ZMS,NPART,NLAST,NSKIP)
      IF(.NOT.STATPT .AND. .NOT.GOTFRQ .AND. MASWRK) WRITE(IW,9010)
