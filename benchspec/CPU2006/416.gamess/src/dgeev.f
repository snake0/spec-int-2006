  200             CONTINUE
  210          CONTINUE
C
            ELSE IF( LSAME( SIDE, 'R' ) ) THEN
C
C              FORM  C * H  OR  C * H'  WHERE  C = ( C1  C2 )
C
C              W := C * V'  =  (C1*V1' + C2*V2')  (STORED IN WORK)
C
C              W := C2
C
               DO 220 J = 1, K
                  CALL DCOPY( M, C( 1, N-K+J ), 1, WORK( 1, J ), 1 )
  220          CONTINUE
