  110             CONTINUE
  120          CONTINUE
            END IF
         END IF
C
      ELSE IF( LSAME( STOREV, 'R' ) ) THEN
C
         IF( LSAME( DIRECT, 'F' ) ) THEN
C
C           LET  V =  ( V1  V2 )    (V1: FIRST K COLUMNS)
C           WHERE  V1  IS UNIT UPPER TRIANGULAR.
C
            IF( LSAME( SIDE, 'L' ) ) THEN
C
C              FORM  H * C  OR  H' * C  WHERE  C = ( C1 )
C                                                  ( C2 )
C
C              W := C' * V'  =  (C1'*V1' + C2'*V2') (STORED IN WORK)
