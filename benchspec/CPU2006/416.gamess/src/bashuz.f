      SUBROUTINE HTWO(E,C,NUCZ)
      IMPLICIT DOUBLE PRECISION(A-H,O-Z)
      DIMENSION E(9),C(9)
C
      IBR=NUCZ-2
      GO TO (100,200,300,400,500,600,700,800), IBR
C
C           LITHIUM
C
  100 CONTINUE
      E(1)=35.04615D+00
      E(2)=5.20169D+00
      E(3)=1.05624D+00
      C(1)=0.0737603D+00
      C(2)=0.3974715D+00
      C(3)=0.6650924D+00
      E(4)=0.71517D+00
      E(5)=0.07053D+00
      E(6)=0.02735D+00
      C(4)=-0.09397D+00
      C(5)=0.5701001D+00
      C(6)=0.4997501D+00
      RETURN
C
C           BERYLLIUM
C
  200 CONTINUE
      E(1)=66.953540D+00
      E(2)=9.9392900D+00
      E(3)=2.05713D+00
      C(1)=0.0702000D+00
      C(2)=0.3919101D+00
      C(3)=0.6699701D+00
      E(4)=1.9122100D+00
      E(5)=0.16132D+00
      E(6)=0.05524D+00
      C(4)=-0.0828204D+00
      C(5)=0.5575528D+00
      C(6)=0.5160426D+00
      RETURN
C
C           BORON
C
  300 CONTINUE
      E(1)=108.43704D+00
