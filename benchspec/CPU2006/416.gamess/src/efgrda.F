     $   (C(LC3,LAT)-EFP(LC3,LEFP))
     $   -CNST3(LC3)*(C(LC2,LAT)-EFP(LC2,LEFP))
 105     CONTINUE
 120    CONTINUE
 140   CONTINUE
      END IF
C
      RETURN
      END
