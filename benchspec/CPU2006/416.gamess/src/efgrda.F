C     FROZEN BUFFER REGION EXCLUDES SOME OF THE NUCLEI... AND SOME
C     ARE INCLUDED ONLY WITH REDUCED CHARGE.
C     LLAT IS A COUNTER, WHILE LAT IS ATOM NUMBER
C
       DO 100 LLAT=1,NAT
       LAT=LLAT
       ZANLAT=ZAN(LAT)
C
       IF(NBUFMO.GT.0)THEN
         IF (LLAT.GT.NATAB+NAPOL) GOTO 100
