        DX=C(1,LAT)-EFC(1,LEFQ)
        DY=C(2,LAT)-EFC(2,LEFQ)
        DZ=C(3,LAT)-EFC(3,LEFQ)
        RS=DX*DX+DY*DY+DZ*DZ
        R5=RS*RS*SQRT(RS)
        R7=RS*R5
        DAMP=EFBTRM(LEFQ)*EXP(-EFATRM(LEFQ)*RS)
C       CNSTNT=ZANLAT*((2*EFATRM(LEFQ)*DAMP/R5-5*(1-DAMP)/R7)
C    &        *(QXX*(3*DX*DX-RS)+QYY*(3*DY*DY-RS)+QZZ*(3*DZ*DZ-RS)
