       DO 120 IFRG=1,NFRG
       DO 120 IIEFQ=1,NMPTS(IFRG)
       LEFQ=LEFQ+1
       IF(.NOT.DOQUAD(LEFQ))GO TO 120
       XX=EFQAD(1,LEFQ)
       YY=EFQAD(2,LEFQ)
       ZZ=EFQAD(3,LEFQ)
       XY=EFQAD(4,LEFQ)
       XZ=EFQAD(5,LEFQ)
       YZ=EFQAD(6,LEFQ)
       DUM=XX+YY+ZZ
       QXX=(3*XX-DUM)/2
       QYY=(3*YY-DUM)/2
       QZZ=(3*ZZ-DUM)/2
       QXY=1.5D+00*XY
       QXZ=1.5D+00*XZ
       QYZ=1.5D+00*YZ
