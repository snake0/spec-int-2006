C*MODULE EFGRDA  *DECK DNUCC
      SUBROUTINE DNUCC(EG3,EF3)
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C
      CHARACTER*8 FRGNME
C
      PARAMETER (MXATM=500, MXAO=2047, MXPT=100, MXFRG=50,
     *           MXFGPT=MXPT*MXFRG)
C
      LOGICAL DOMONO,DODIPO,DOQUAD,DOOCTU
C
      DIMENSION EG3(3,*),EF3(3,*)
C
      COMMON /DOMULT/ DOMONO(MXFGPT),DODIPO(MXFGPT),DOQUAD(MXFGPT),
     *                DOOCTU(MXFGPT)
      COMMON /EFMULT/ EFC(3,MXFGPT),EFCHG(2,MXFGPT),EFATRM(MXFGPT),
     *                EFBTRM(MXFGPT),EFATRM2(MXFGPT),EFBTRM2(MXFGPT),
     *                EFDIP(3,MXFGPT),EFQAD(6,MXFGPT),
     *                EFOCT(10,MXFGPT),FRGNME(MXFGPT)
      COMMON /EFPBUF/ POLCHG(10),NBUFMO,LBUFF(MXAO),LBF,NAPOL,IAPOL(10)
      COMMON /FRGINF/ NMPTS(MXFRG),NMTTPT,IEFC,IEFD,IEFQ,IEFO,
     *                NPPTS(MXFRG),NPTTPT,IEFP,
     *                NRPTS(MXFRG),NRTTPT,IREP,ICHGP,NFRG
      COMMON /INFOA / NAT,ICH,MUL,NUM,NQMT,NE,NA,NB,
     *                ZAN(MXATM),C(3,MXATM)
C
C     EFC - NUCLEI CONTRIBUTION TO NUCLEAR GRADIENTS.
C
C
C     FROZEN BUFFER REGION EXCLUDES SOME OF THE NUCLEI... AND SOME
C     ARE INCLUDED ONLY WITH REDUCED CHARGE.
C
C     LLAT IS A COUNTER, WHILE LAT IS ATOM NUMBER
C
       IF(NBUFMO.GT.0) NATAB=NAT-LBF
       DO 305 LLAT=1,NAT
        LAT=LLAT
        ZANLAT=ZAN(LAT)
C
        IF(NBUFMO.GT.0)THEN
         IF (LLAT.GT.NATAB+NAPOL) GOTO 305
         IF (LLAT.GT.NATAB) THEN
            LAT=IAPOL(LLAT-NATAB)
            ZANLAT=POLCHG(LLAT-NATAB)
         END IF
        END IF
C
        DO 300 LEFC=1,NMTTPT
         IF(.NOT.DOMONO(LEFC))GO TO 300
         RS=(C(1,LAT)-EFC(1,LEFC))**2+(C(2,LAT)-EFC(2,LEFC))**2+
     *      (C(3,LAT)-EFC(3,LEFC))**2
         DAMP=-EFBTRM(LEFC)*EXP(-EFATRM(LEFC)*RS)
         CNSTNT=ZANLAT*EFCHG(1,LEFC)*((1+DAMP)/RS+2*EFATRM(LEFC)
     *         *DAMP)/SQRT(RS)
         CNSTNT=CNSTNT + ZANLAT*EFCHG(2,LEFC)/(RS*SQRT(RS))
         DO 295 LC=1,3
          EG3(LC,LAT)=EG3(LC,LAT)+(EFC(LC,LEFC)-C(LC,LAT))*CNSTNT
CPAUL
          EF3(LC,LEFC)=EF3(LC,LEFC)-(EFC(LC,LEFC)-C(LC,LAT))*CNSTNT
CPAUL
 295     CONTINUE
 300    CONTINUE
 305   CONTINUE
C
      RETURN
      END
C*MODULE EFGRDA  *DECK DNUCD
      SUBROUTINE DNUCD(EG3,EF3)
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C
      CHARACTER*8 FRGNME
C
      DIMENSION EG3(3,*),EF3(3,*),CNST3(3)
C
      PARAMETER (MXATM=500, MXAO=2047, MXPT=100, MXFRG=50,
     *           MXFGPT=MXPT*MXFRG)
C
      LOGICAL DOMONO,DODIPO,DOQUAD,DOOCTU
C
      COMMON /DOMULT/ DOMONO(MXFGPT),DODIPO(MXFGPT),DOQUAD(MXFGPT),
     *                DOOCTU(MXFGPT)
      COMMON /EFMULT/ EFC(3,MXFGPT),EFCHG(2,MXFGPT),EFATRM(MXFGPT),
     *                EFBTRM(MXFGPT),EFATRM2(MXFGPT),EFBTRM2(MXFGPT),
     *                EFDIP(3,MXFGPT),EFQAD(6,MXFGPT),
     *                EFOCT(10,MXFGPT),FRGNME(MXFGPT)
      COMMON /EFPBUF/ POLCHG(10),NBUFMO,LBUFF(MXAO),LBF,NAPOL,IAPOL(10)
      COMMON /FGRAD / DEF(3,MXFGPT),DEFT(3,MXFRG),TORQ(3,MXFRG),
     *                EFCENT(3,MXFRG),FRGMAS(MXFRG),FRGMI(6,MXFRG),
     *                ATORQ(3,MXFRG)
      COMMON /FRGINF/ NMPTS(MXFRG),NMTTPT,IEFC,IEFD,IEFQ,IEFO,
     *                NPPTS(MXFRG),NPTTPT,IEFP,
     *                NRPTS(MXFRG),NRTTPT,IREP,ICHGP,NFRG
      COMMON /INFOA / NAT,ICH,MUL,NUM,NQMT,NE,NA,NB,
     *                ZAN(MXATM),C(3,MXATM)
C
C     EFD - NUCLEI CONTRIBUTION TO NUCLEAR GRADIENTS.
C
C
C     FROZEN BUFFER REGION EXCLUDES SOME OF THE NUCLEI... AND SOME
C     ARE INCLUDED ONLY WITH REDUCED CHARGE.
C     LLAT IS A COUNTER, WHILE LAT IS ATOM NUMBER
C
       IF(NBUFMO.GT.0) NATAB=NAT-LBF
       DO 140 LLAT=1,NAT
       LAT=LLAT
       ZANLAT=ZAN(LAT)
C
       IF(NBUFMO.GT.0)THEN
         IF (LLAT.GT.NATAB+NAPOL) GOTO 140
         IF (LLAT.GT.NATAB) THEN
            LAT=IAPOL(LLAT-NATAB)
            ZANLAT=POLCHG(LLAT-NATAB)
         END IF
       END IF
C
       LEFD=0
       DO 120 IFRG=1,NFRG
       DO 120 IIEFD=1,NMPTS(IFRG)
       LEFD=LEFD+1
        IF(.NOT.DODIPO(LEFD))GO TO 120
        RS=(C(1,LAT)-EFC(1,LEFD))**2+(C(2,LAT)-EFC(2,LEFD))**2+
     *     (C(3,LAT)-EFC(3,LEFD))**2
        R3=RS*SQRT(RS)
        DAMP=EFBTRM(LEFD)*EXP(-EFATRM(LEFD)*RS)
        CNSTT1=ZANLAT*(3*(1-DAMP)/(R3*RS)-2*EFATRM(LEFD)*DAMP/R3)*
     *         (EFDIP(1,LEFD)*(C(1,LAT)-EFC(1,LEFD))+
     *          EFDIP(2,LEFD)*(C(2,LAT)-EFC(2,LEFD))+
     *          EFDIP(3,LEFD)*(C(3,LAT)-EFC(3,LEFD)))
        CNSTT2=ZANLAT*(1-DAMP)/R3
        DO 100 LC=1,3
         CNST3(LC)=CNSTT2*EFDIP(LC,LEFD)
         CNST4=CNSTT1*(EFC(LC,LEFD)-C(LC,LAT))
         CNST5=CNST3(LC)+CNST4
         EG3(LC,LAT)=EG3(LC,LAT)+CNST5
         EF3(LC,LEFD)=EF3(LC,LEFD)-CNST5
 100    CONTINUE
         DO 105 LC=1,3
         LCM=LC-1
         LC2=MOD(LCM+1,3)+1
         LC3=MOD(LCM+2,3)+1
         ATORQ(LC,IFRG)=ATORQ(LC,IFRG)+CNST3(LC2)*
     $   (C(LC3,LAT)-EFC(LC3,LEFD))
     $   -CNST3(LC3)*(C(LC2,LAT)-EFC(LC2,LEFD))
 105     CONTINUE
 120   CONTINUE
 140  CONTINUE
      RETURN
      END
C*MODULE EFGRDA  *DECK DNUCO
      SUBROUTINE DNUCO(EG3,EF3)
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C
      CHARACTER*8 FRGNME
C
      PARAMETER (MXATM=500, MXAO=2047, MXPT=100, MXFRG=50,
     *           MXFGPT=MXPT*MXFRG)
C
      DIMENSION EG3(3,*),EF3(3,*)
C
      LOGICAL DOMONO,DODIPO,DOQUAD,DOOCTU
C
      COMMON /DOMULT/ DOMONO(MXFGPT),DODIPO(MXFGPT),DOQUAD(MXFGPT),
     *                DOOCTU(MXFGPT)
      COMMON /EFMULT/ EFC(3,MXFGPT),EFCHG(2,MXFGPT),EFATRM(MXFGPT),
     *                EFBTRM(MXFGPT),EFATRM2(MXFGPT),EFBTRM2(MXFGPT),
     *                EFDIP(3,MXFGPT),EFQAD(6,MXFGPT),
     *                EFOCT(10,MXFGPT),FRGNME(MXFGPT)
      COMMON /EFPBUF/ POLCHG(10),NBUFMO,LBUFF(MXAO),LBF,NAPOL,IAPOL(10)
      COMMON /FGRAD / DEF(3,MXFGPT),DEFT(3,MXFRG),TORQ(3,MXFRG),
     *                EFCENT(3,MXFRG),FRGMAS(MXFRG),FRGMI(6,MXFRG),
     *                ATORQ(3,MXFRG)
      COMMON /FRGINF/ NMPTS(MXFRG),NMTTPT,IEFC,IEFD,IEFQ,IEFO,
     *                NPPTS(MXFRG),NPTTPT,IEFP,
     *                NRPTS(MXFRG),NRTTPT,IREP,ICHGP,NFRG
      COMMON /INFOA / NAT,ICH,MUL,NUM,NQMT,NE,NA,NB,
     *                ZAN(MXATM),C(3,MXATM)
C
      DATA ONE/1.0D+00/
      DATA PT5,THREE/0.5D+00,3.0D+00/
      DATA FIVE,SIX/5.0D+00,6.0D+00/
      DATA GLIM/1.0D-14/
      DATA QLIM/1.0D-07/
C
C     EFO - NUCLEI CONTRIBUTION TO NUCLEAR GRADIENTS.
C
       I=0
       IF(NBUFMO.GT.0) NATAB=NAT-LBF
       DO 380 IFRG=1,NFRG
       DO 380 IIEFQ=1,NMPTS(IFRG)
       I=I+1
C     DO 380 I = 1,NMTTPT
      IF(.NOT.DOOCTU(I))GO TO 380
      ALFA = EFATRM(I)
      BETA = EFBTRM(I)
C
C REPLACE THIRD MOMENTS IN ARRAY SM WITH ELECTRIC OCTUPOLE TENSORS
C
      XXX2 = EFOCT(1,I)*PT5
      YYY2 = EFOCT(2,I)*PT5
      ZZZ2 = EFOCT(3,I)*PT5
      XXY2 = EFOCT(4,I)*PT5
      XXZ2 = EFOCT(5,I)*PT5
      XYY2 = EFOCT(6,I)*PT5
      YYZ2 = EFOCT(7,I)*PT5
      XZZ2 = EFOCT(8,I)*PT5
      YZZ2 = EFOCT(9,I)*PT5
      XYZ2 = EFOCT(10,I)*PT5
      DUMX = XXX2 + XYY2 + XZZ2
      DUMY = XXY2 + YYY2 + YZZ2
      DUMZ = XXZ2 + YYZ2 + ZZZ2
      XXX2 = FIVE * XXX2 - THREE * DUMX
      YYY2 = FIVE * YYY2 - THREE * DUMY
      ZZZ2 = FIVE * ZZZ2 - THREE * DUMZ
      XXY2 = FIVE * XXY2 - DUMY
      XXZ2 = FIVE * XXZ2 - DUMZ
      XYY2 = FIVE * XYY2 - DUMX
      YYZ2 = FIVE * YYZ2 - DUMZ
      XZZ2 = FIVE * XZZ2 - DUMX
      YZZ2 = FIVE * YZZ2 - DUMY
      XYZ2 = FIVE * XYZ2
C
C     FROZEN BUFFER REGION EXCLUDES SOME OF THE NUCLEI... AND SOME
C     ARE INCLUDED ONLY WITH REDUCED CHARGE.
C     JJ IS A COUNTER, WHILE J IS ATOM NUMBER
C
      DO 370 JJ = 1,NAT
       J=JJ
       ZANJ=ZAN(J)
C
       IF(NBUFMO.GT.0)THEN
         IF (JJ.GT.NATAB+NAPOL) GOTO 370
         IF (JJ.GT.NATAB) THEN
            J=IAPOL(JJ-NATAB)
            ZANJ=POLCHG(JJ-NATAB)
         END IF
       END IF
C
      X = C(1,J) - EFC(1,I)
      Y = C(2,J) - EFC(2,I)
      Z = C(3,J) - EFC(3,I)
      XX = X*X
      YY = Y*Y
      ZZ = Z*Z
      XY=X*Y
      XZ=X*Z
      YZ=Y*Z
      XXX = XX*X
      YYY = YY*Y
      ZZZ = ZZ*Z
      XXY = XX*Y
      XXZ = XX*Z
      XYY = X*YY
      YYZ = YY*Z
      XZZ = X*ZZ
      YZZ = Y*ZZ
      XYZ = X*Y*Z
      R2 = XX + YY + ZZ
      IF(R2.LT.GLIM) GO TO 370
      R7 = R2 * R2 * R2 * SQRT(R2)
      RRX = R2 * X
      RRY = R2 * Y
      RRZ = R2 * Z
      TERM = XXX2 * (FIVE*XXX - THREE*RRX)
     *     + YYY2 * (FIVE*YYY - THREE*RRY)
     *     + ZZZ2 * (FIVE*ZZZ - THREE*RRZ)
     *     + XXY2 * (FIVE*XXY - RRY)*THREE
     *     + XXZ2 * (FIVE*XXZ - RRZ)*THREE
     *     + XYY2 * (FIVE*XYY - RRX)*THREE
     *     + YYZ2 * (FIVE*YYZ - RRZ)*THREE
     *     + XZZ2 * (FIVE*XZZ - RRX)*THREE
     *     + YZZ2 * (FIVE*YZZ - RRY)*THREE
     *     + XYZ2 * (FIVE*XYZ)*SIX
      FACT = ONE
      DFACT=0
      IF(ABS(ALFA).GT.QLIM)THEN
       FACT = ONE - (BETA * EXP (-ALFA * R2))
       DFACT=2*ALFA*BETA*EXP(-ALFA*R2)
      END IF
      CNST1=ZANJ*TERM*(DFACT-7*FACT/R2)/(R7*5)
      CNSTX = ZANJ*FACT*
     * (XXX2*(15*XX)+30*XXY2*XY+30*XXZ2*XZ+
     *  XYY2*(15*YY)+XZZ2*(15*ZZ)
     *  +30*XYZ2*YZ)/(5*R7)
      CNSTXB = ZANJ*FACT*
     * (XXX2*(-6*XX)-6*YYY2*XY-6*ZZZ2*XZ-6*XXY2*XY-6*XXZ2*XZ+
     *  XYY2*(-6*XX)-6*YYZ2*XZ+XZZ2*(-6*XX)-
     *  6*YZZ2*XY)/(5*R7)
      CNSTY = ZANJ*FACT*
     * (YYY2*(15*YY)+30*YYZ2*YZ+30*XYY2*XY+
     *  YZZ2*(15*ZZ)+XXY2*(15*XX)
     *  +30*XYZ2*XZ)/(5*R7)
      CNSTYB = ZANJ*FACT*
     * (YYY2*(-6*YY)-6*ZZZ2*YZ-6*XXX2*XY-6*YYZ2*YZ-6*XYY2*XY+
     *  YZZ2*(-6*YY)-6*XZZ2*XY+XXY2*(-6*YY)-
     *  6*XXZ2*YZ)/(5*R7)
      CNSTZ = ZANJ*FACT*
     * (ZZZ2*(15*ZZ)+30*XZZ2*XZ+30*YZZ2*YZ+
     *  XXZ2*(15*XX)+YYZ2*(15*YY)
     *  +30*XYZ2*XY)/(5*R7)
      CNSTZB = ZANJ*FACT*
     * (ZZZ2*(-6*ZZ)-6*XXX2*XZ-6*YYY2*YZ-6*XZZ2*XZ-6*YZZ2*YZ+
     *  XXZ2*(-6*ZZ)-6*XXY2*YZ+YYZ2*(-6*ZZ)-
     *  6*XYY2*XZ)/(5*R7)
C
      EG3(1,J)=EG3(1,J)+(CNST1*X+CNSTX+CNSTXB)
      EG3(2,J)=EG3(2,J)+(CNST1*Y+CNSTY+CNSTYB)
      EG3(3,J)=EG3(3,J)+(CNST1*Z+CNSTZ+CNSTZB)
      EF3(1,I)=EF3(1,I)-(CNST1*X+CNSTX+CNSTXB)
      EF3(2,I)=EF3(2,I)-(CNST1*Y+CNSTY+CNSTYB)
      EF3(3,I)=EF3(3,I)-(CNST1*Z+CNSTZ+CNSTZB)
       ATORQ(1,IFRG)=ATORQ(1,IFRG)+CNSTY*Z-CNSTZ*Y
       ATORQ(2,IFRG)=ATORQ(2,IFRG)+CNSTZ*X-CNSTX*Z
       ATORQ(3,IFRG)=ATORQ(3,IFRG)+CNSTX*Y-CNSTY*X
CPAUL
 370   CONTINUE
 380   CONTINUE
      RETURN
      END
C*MODULE EFGRDA  *DECK DNUCP
      SUBROUTINE DNUCP(EG3,EF3)
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
