C    *       ((DIND(1,LEFP))*(C(1,LAT)-EFP(1,LEFP))+
C    *           (DIND(2,LEFP))*(C(2,LAT)-EFP(2,LEFP))+
C    *           (DIND(3,LEFP))*(C(3,LAT)-EFP(3,LEFP)))
C    *           /(R3*RS)
         CNSTT2=ZANLAT/R3
C
         DO 100 LC=1,3
         CNST3(LC)=CNSTT2*PT5*(DIND(LC,LEFP)+DINDD(LC,LEFP))
C        CNST3(LC)=CNSTT2*DIND(LC,LEFP)
         CNST4=CNSTT1*(EFP(LC,LEFP)-C(LC,LAT))
         CNST5=CNST3(LC)+CNST4
         EG3(LC,LAT)=EG3(LC,LAT)+CNST5
CPAUL
