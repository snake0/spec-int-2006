C     A function to return the environment variable values that the program expects,
C     without calling the real GETENV() and without setting the environment vars
C     using the C function envvar_()

C These are the array indices of the 
C variables
#define ENV_INDEX_MAX 35

#define ENV_EXTBAS_NUM 1
#define ENV_IRCDATA_NUM 2
#define ENV_INPUT_NUM 3
#define ENV_OUTPUT_NUM 4
#define ENV_PUNCH_NUM 5
#define ENV_AOINTS_NUM 6
#define ENV_MOINTS_NUM 7
#define ENV_DICTNRY_NUM 8
#define ENV_WORK15_NUM 9
#define ENV_WORK16_NUM 10
#define ENV_FOCKDER_NUM 11
#define ENV_PCMDATA_NUM 12
#define ENV_DASORT_NUM 13
#define ENV_EIGENVFILE_NUM 14
#define ENV_CCREST_NUM 15
#define ENV_CCDIIS_NUM 16
#define ENV_CCINTS_NUM 17
#define ENV_CCT1AMP_NUM 18
#define ENV_CCT2AMP_NUM 19
#define ENV_CCT3AMP_NUM 20
#define ENV_CCVM_NUM 21
#define ENV_CCVE_NUM 22
#define ENV_ORDINT_NUM 23
#define ENV_DAFL30_NUM 24
#define ENV_JKFILE_NUM 25
#define ENV_CASINTS_NUM 26
#define ENV_CIVECTR_NUM 27
#define ENV_AABB41_NUM 28
#define ENV_BBAA42_NUM 29
#define ENV_BBBB43_NUM 30

      SUBROUTINE DUMMYSETENVVARS

      COMMON /ENVVARS/ ENVVARVALUE 
      CHARACTER*32 ENVVARVALUE
      DIMENSION ENVVARVALUE(ENV_INDEX_MAX)

      CHARACTER*32 ENVVAR, VALUE
      INTEGER INDEX

C  Read the env parameters from the command prompt   

   20 READ (*, *, END=50) ENVVAR, VALUE
C         WRITE (*,*) ENVVAR, VALUE
         CALL GETENVINDEX(ENVVAR, INDEX)
         ENVVARVALUE(INDEX) = VALUE
         GO TO 20
   50 ENVVARVALUE(ENV_INDEX_MAX) = ' '
      END
      

      SUBROUTINE DUMMYGETENV (ENVVAR, VALUE)

      COMMON /ENVVARS/ ENVVARVALUE 
      CHARACTER*32 ENVVARVALUE
      DIMENSION ENVVARVALUE(ENV_INDEX_MAX)

      CHARACTER*(*) ENVVAR, VALUE
      INTEGER INDEX

C      WRITE (*,*) ENVVAR
      CALL GETENVINDEX(ENVVAR, INDEX)

      VALUE = ENVVARVALUE(INDEX)

      END


C     Subroutine to return the index of a ENVVAR in 
C     in the array ENVVARVALUE

      SUBROUTINE GETENVINDEX(ENVVAR, INDEX)

      CHARACTER*(*) ENVVAR
      INTEGER INDEX


      IF (ENVVAR.EQ.'EXTBAS') THEN
         INDEX = ENV_EXTBAS_NUM
      ELSE IF (ENVVAR.EQ.'IRCDATA') THEN
         INDEX = ENV_IRCDATA_NUM
      ELSE IF (ENVVAR.EQ.'INPUT') THEN
         INDEX = ENV_INPUT_NUM
      ELSE IF (ENVVAR.EQ.'OUTPUT') THEN
         INDEX = ENV_OUTPUT_NUM
      ELSE IF (ENVVAR.EQ.'PUNCH') THEN
         INDEX = ENV_PUNCH_NUM
      ELSE IF (ENVVAR.EQ.'AOINTS') THEN
         INDEX = ENV_AOINTS_NUM
      ELSE IF (ENVVAR.EQ.'MOINTS') THEN
         INDEX = ENV_MOINTS_NUM
      ELSE IF (ENVVAR.EQ.'DICTNRY') THEN
         INDEX = ENV_DICTNRY_NUM
      ELSE IF (ENVVAR.EQ.'WORK15') THEN
         INDEX = ENV_WORK15_NUM
      ELSE IF (ENVVAR.EQ.'WORK16') THEN
         INDEX = ENV_WORK16_NUM
      ELSE IF (ENVVAR.EQ.'FOCKDER') THEN
         INDEX = ENV_FOCKDER_NUM
      ELSE IF (ENVVAR.EQ.'PCMDATA') THEN
         INDEX = ENV_PCMDATA_NUM
      ELSE IF (ENVVAR.EQ.'DASORT') THEN
         INDEX = ENV_DASORT_NUM
      ELSE IF (ENVVAR.EQ.'CCREST') THEN
         INDEX = ENV_CCREST_NUM
      ELSE IF (ENVVAR.EQ.'CCDIIS') THEN
         INDEX = ENV_CCDIIS_NUM
      ELSE IF (ENVVAR.EQ.'CCINTS') THEN
         INDEX = ENV_CCINTS_NUM
      ELSE IF (ENVVAR.EQ.'CCT1AMP') THEN
         INDEX = ENV_CCT1AMP_NUM
      ELSE IF (ENVVAR.EQ.'CCT2AMP') THEN
         INDEX = ENV_CCT2AMP_NUM
      ELSE IF (ENVVAR.EQ.'CCT3AMP') THEN
         INDEX = ENV_CCT3AMP_NUM
      ELSE IF (ENVVAR.EQ.'CCVM') THEN
         INDEX = ENV_CCVM_NUM
      ELSE IF (ENVVAR.EQ.'CCVE') THEN
         INDEX = ENV_CCVE_NUM
      ELSE IF (ENVVAR.EQ.'ORDINT') THEN
         INDEX = ENV_ORDINT_NUM
      ELSE IF (ENVVAR.EQ.'EIGENVFILE') THEN
         INDEX = ENV_EIGENVFILE_NUM
      ELSE IF (ENVVAR.EQ.'DAFL30') THEN
         INDEX = ENV_DAFL30_NUM
      ELSE IF (ENVVAR.EQ.'JKFILE') THEN
         INDEX = ENV_JKFILE_NUM
      ELSE IF (ENVVAR.EQ.'CASINTS') THEN
         INDEX = ENV_CASINTS_NUM
      ELSE IF (ENVVAR.EQ.'CIVECTR') THEN
         INDEX = ENV_CIVECTR_NUM
      ELSE IF (ENVVAR.EQ.'AABB41') THEN
         INDEX = ENV_AABB41_NUM
      ELSE IF (ENVVAR.EQ.'BBAA42') THEN
         INDEX = ENV_BBAA42_NUM
      ELSE IF (ENVVAR.EQ.'BBBB43') THEN
         INDEX = ENV_BBBB43_NUM
      ELSE
         INDEX = ENV_INDEX_MAX
      END IF

      END
