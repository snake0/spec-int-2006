#include <iostream>
#include <assert.h>

namespace soplex
{

bool msginconsistent(const char* name, const char* file, int line)
{
   assert(name != 0);
   assert(file != 0);
   assert(line >= 0);

   std::cout << file << "(" << line << ") "
             << "Inconsistency detected in " << name << std::endl; 

   return 0;
}
} // namespace soplex

